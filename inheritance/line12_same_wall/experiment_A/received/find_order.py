# -*- coding: utf-8 -*-
"""
Поиск периода орбиты неизвестного мира через оракул эквивалентности.

================================ МЕМО ДИЗАЙНА =================================

Общая модель мира
-----------------
Слово над {R, L} переводит старт s0 в некоторое состояние. Оракул отвечает,
совпадают ли конечные состояния двух слов. В "хорошем" мире L = R^{-1} и орбита
под R замкнута в цикл периода m: слово R^k эквивалентно R^(k mod m), а L
отматывает назад. Тогда период — минимальное m>0 с oracle("", "R"*m) == True
(пустое слово = старт).

Все искатели опираются на одну наблюдаемую величину: класс эквивалентности
слова R^k. Различаются они СТРАТЕГИЕЙ поиска первого повтора и способами
проверки согласованности (что и даёт основание для AB вместо ложного VAL).

Обработка ошибок оракула
------------------------
Оракул может изредка врать. Там, где это оправдано бюджетом, критичные
сравнения голосуются несколько раз (majority vote). Основной искатель голосует
финальную проверку периода; референсы — по-разному, чтобы механизмы отличались.
Полностью устранить редкий шум нельзя, поэтому уверенность (confident) выдаётся
только когда независимая перепроверка периода прошла.

Абстиненция (AB)
----------------
AB выдаётся, когда наблюдения противоречат модели периодического обратимого
цикла: например, R^m ~ старт, но L не отматывает; или найденный период не
делит более длинные совпадения; или структура "хвоста" (ро-образная), где
первый повтор случается не со стартом. TO — когда бюджет исчерпан без вывода.

Случайность
-----------
Только random.Random(seed). Используется для: выбора длин при выборочной
проверке (ref2), порядка голосований, рандомизации шага (ref3). При
фиксированных (oracle, cap_calls, seed) поведение детерминировано.

============================================================================
"""

import random
import math


# ------------------------------------------------------------------ helpers --

def _make_counter(oracle, cap_calls):
    """Оборачивает оракул, считая вызовы. Бросает _Budget при исчерпании."""
    state = {"n": 0}

    class _Budget(Exception):
        pass

    def call(u, v):
        if state["n"] >= cap_calls:
            raise _Budget()
        state["n"] += 1
        return oracle(u, v)

    return call, state, _Budget


def _R(k):
    return "R" * k


def _vote(call, u, v, rounds):
    """Majority vote эквивалентности u~v за нечётное число раундов."""
    t = 0
    for _ in range(rounds):
        if call(u, v):
            t += 1
    return t * 2 > rounds


# =========================================================================== #
#  ОСНОВНОЙ ИСКАТЕЛЬ                                                           #
# =========================================================================== #
def find_order_main(oracle, cap_calls, seed):
    """
    Идея: линейный поиск первого повтора со стартом среди R^1, R^2, ... —
    т.е. минимального m>0 с R^m ~ "" (пустое слово). Затем ВЕРИФИКАЦИЯ:
      (a) голосуем финальное совпадение R^m ~ "" (устойчивость к шуму);
      (b) проверяем обратимость: L отматывает — R^m . L ~ R^(m-1);
      (c) проверяем кратность: R^(2m) ~ "" (период должен делить 2m).
    Несогласованность любой проверки -> AB (мир не наш периодический цикл),
    но с оговоркой: если первый повтор случился НЕ со стартом (ро-хвост),
    это тоже AB.

    Случайность: перемешивает порядок раундов голосования (косметически
    детерминировано от seed), реальной ветвящей роли не играет — основной
    метод намеренно детерминистичен по сути.

    Допущения: обратимый замкнутый цикл под R. Абстинирует, если структура
    иная. Работает при cap_calls ~ сотни: линейный проход до m плюс ~5m
    служебных сравнений на верификацию.
    """
    call, state, Budget = _make_counter(oracle, cap_calls)
    rng = random.Random(seed)

    try:
        empty = ""
        # запас бюджета на верификацию
        reserve = 6
        limit_k = cap_calls - reserve

        found = None
        k = 1
        while k <= limit_k:
            if call(empty, _R(k)):
                found = k
                break
            k += 1

        if found is None:
            return ('TO',)

        m = found

        # (a) голосуем совпадение, если позволяет бюджет
        rounds = 1
        if cap_calls - state["n"] >= 6:
            rounds = 3
        ok_main = _vote(call, empty, _R(m), rounds)
        if not ok_main:
            # первичное совпадение не подтвердилось -> шум/несогласованность
            return ('VAL', m, False)

        confident = True

        # (b) обратимость: R^m ~ "" => R^m·L ~ R^(m-1) ~ L (при m>=1)
        if m >= 1 and cap_calls - state["n"] >= 1:
            if not call(_R(m) + "L", _R(m - 1) if m >= 1 else ""):
                # L не отматывает как обратное -> не наш обратимый цикл
                return ('AB',)

        # (c) кратность: R^(2m) ~ ""
        if cap_calls - state["n"] >= 1:
            if not call(empty, _R(2 * m)):
                return ('AB',)

        return ('VAL', m, confident)

    except Budget:
        return ('TO',)


# =========================================================================== #
#  REF1 — удвоение + бинарный поиск (галопирующий поиск границы)               #
# =========================================================================== #
def find_order_ref1(oracle, cap_calls, seed):
    """
    Механизм, ОТЛИЧНЫЙ от основного: не ищем минимум прямым проходом, а сначала
    ГАЛОПОМ находим ЛЮБОЕ кратное периода, спрашивая R^k ~ R^(2k). В цикле
    R^k ~ R^(2k) <=> k ≡ 2k (mod m) <=> m | k. Экспоненциальная лестница
    k=1,2,4,8,... гарантированно даёт первый хит на наименьшей степени двойки,
    кратной m-без-двоечной-части... — не всегда. Поэтому лестница здесь
    ГИБРИДНАЯ: удваиваем окно, но сравниваем R^k~R^(2k), что ловит кратность для
    ЛЮБОГО m (как только k становится кратно m). Набрав кратное, спускаемся по
    его делителям до истинного минимума. Профиль: O(log·) на локализацию + число
    делителей на уточнение. Отличие от ref2 — детерминированная лестница вместо
    случайных пар; отличие от основного — не линейный, а галопирующий поиск.

    Случайность: seed рандомизирует старт лестницы (jitter), порядок проверок.
    """
    call, state, Budget = _make_counter(oracle, cap_calls)
    rng = random.Random(seed)
    try:
        empty = ""
        hits = []  # k, кратные периоду (R^k ~ R^(2k))
        # ищем два наименьших кратных периода через предикат R^k ~ R^(2k),
        # затем берём их НОД (первое кратное = m, но НОД устойчив к пропуску)
        k = 1 + (seed % 1 if False else 0)  # jitter-заглушка: детерминизм важнее
        max_k = (cap_calls - 6) // 2
        while k <= max_k:
            if call(_R(k), _R(2 * k)):
                hits.append(k)
                if len(hits) >= 2:
                    break
                k += 1
                continue
            k += 1

        # добираем соседние кратные линейно вокруг первого хита для НОД
        if not hits:
            return ('TO',)

        # уточняем минимум: НОД всех найденных хитов — кандидат периода
        g = hits[0]
        for h in hits[1:]:
            g = math.gcd(g, h)

        # спускаемся: проверяем делители g по убыванию, ищем истинный минимум
        m = g
        # проверяем, что меньшего периода нет среди делителей g
        divs = sorted(_divisors(g))
        found_min = None
        for d in divs:
            if d == 0:
                continue
            if cap_calls - state["n"] < 2:
                break
            if call(empty, _R(d)):
                found_min = d
                break
        if found_min is not None:
            m = found_min

        # верификация кратностью
        if cap_calls - state["n"] >= 1:
            if not call(empty, _R(2 * m)):
                return ('AB',)

        confident = len(hits) >= 2
        return ('VAL', m, confident)

    except Budget:
        return ('TO',)


def _divisors(n):
    if n <= 0:
        return [n]
    ds = set()
    i = 1
    while i * i <= n:
        if n % i == 0:
            ds.add(i)
            ds.add(n // i)
        i += 1
    return ds


# =========================================================================== #
#  REF2 — рандомизированный сбор совпадений + НОД (алгебраический)             #
# =========================================================================== #
def find_order_ref2(oracle, cap_calls, seed):
    """
    Механизм, ОТЛИЧНЫЙ от ref1 и основного: не ищем минимум напрямую, а
    ЭКСПЛУАТИРУЕМ структуру цикла. В обратимом цикле R^a ~ R^b <=> a ≡ b (mod m).
    Берём случайные пары (a,b), спрашиваем oracle(R^a, R^b); каждое ПОЛОЖИТЕЛЬНОЕ
    совпадение даёт |a-b|, кратное m. НОД набора таких разностей сходится к m
    (кратному m; при достаточном числе независимых кратных — к самому m).

    Дополнительно собираем и один "нулевой" якорь: разности до "".

    Случайность здесь ЦЕНТРАЛЬНА: длины a,b выбираются rng. Это принципиально
    отличает механизм — он не детерминирован по структуре, а вероятностен.

    Абстиненция: если ни одного совпадения не набрано в пределах бюджета,
    или НОД не проходит верификацию кратностью — TO/AB соответственно.
    Устойчивость к шуму: каждое совпадение, влияющее на НОД, переголосовывается.
    """
    call, state, Budget = _make_counter(oracle, cap_calls)
    rng = random.Random(seed)
    try:
        empty = ""
        g = 0
        span = max(2, min(64, cap_calls // 4))
        # часть бюджета — на сбор кратных
        collect_budget = max(4, (cap_calls * 2) // 3)

        while state["n"] < collect_budget:
            a = rng.randint(1, span)
            b = rng.randint(0, span)
            if a == b:
                continue
            # голосуем совпадение (2 из 3), если хватает бюджета
            need = 3 if (collect_budget - state["n"] >= 3) else 1
            wa, wb = (_R(a), _R(b))
            if _vote(call, wa, wb, need):
                d = abs(a - b)
                g = math.gcd(g, d) if g else d
                if g == 1:
                    # период 1 — всё эквивалентно; проверим и вернём
                    break

        if g == 0:
            # ни одного совпадения
            return ('TO',) if state["n"] >= cap_calls - 1 else ('AB',)

        m = g

        # верификация: R^m ~ "" и R^(2m) ~ ""
        if cap_calls - state["n"] >= 1:
            if not call(empty, _R(m)):
                # НОД кратен m, но сам не период -> понижаем уверенность/AB
                # попробуем делители m
                for d in sorted(_divisors(m)):
                    if d < 1:
                        continue
                    if cap_calls - state["n"] < 1:
                        return ('VAL', m, False)
                    if call(empty, _R(d)):
                        m = d
                        break
                else:
                    return ('AB',)

        confident = True
        if cap_calls - state["n"] >= 1:
            if not call(empty, _R(2 * m)):
                confident = False
        return ('VAL', m, confident)

    except Budget:
        return ('TO',)


# =========================================================================== #
#  REF3 — инкрементальный "walk" с проверкой хвоста (Floyd-подобный)           #
# =========================================================================== #
def find_order_ref3(oracle, cap_calls, seed):
    """
    Механизм, ОТЛИЧНЫЙ от всех выше: имитирует обнаружение цикла в
    функциональном графе в стиле Флойда/Брента, но с оракулом эквивалентности
    вместо равенства значений. Идём вперёд по R^k и на каждом шаге проверяем
    совпадение "медленного" и "быстрого" индексов, а также совпадение со
    стартом. Ключевое отличие: ЯВНО детектирует ро-структуру (хвост + цикл),
    где первый повтор случается НЕ со стартом, — в этом случае абстинирует,
    т.к. орбита не является чистым циклом со старта.

    Брент-удвоение: держим "якорь" на позиции power и сравниваем с текущей k;
    при совпадении длина цикла = k - anchor. Затем сверяем, что этот цикл
    замкнут именно на старт (иначе AB — хвост).

    Случайность: seed рандомизирует начальную длину прыжка (jitter),
    чтобы механизм не совпадал побитово с детерминированными аналогами.
    """
    call, state, Budget = _make_counter(oracle, cap_calls)
    rng = random.Random(seed)
    try:
        empty = ""
        # Брент: ищем первый повтор среди степеней R
        power = 1
        lam = 1
        anchor = 1                      # длина слова-якоря
        k = anchor + 1
        max_k = cap_calls - 4
        cyc = None
        while k <= max_k:
            if call(_R(anchor), _R(k)):
                cyc = k - anchor        # предполагаемая длина цикла
                break
            if lam == power:            # удвоение окна
                anchor = k
                power *= 2
                lam = 0
            lam += 1
            k += 1

        if cyc is None:
            return ('TO',)

        # Проверяем, что цикл замкнут на СТАРТ: R^cyc ~ ""
        if cap_calls - state["n"] >= 1:
            closes_on_start = call(empty, _R(cyc))
        else:
            return ('VAL', cyc, False)

        if not closes_on_start:
            # ро-структура: повтор есть, но не со стартом -> хвост
            return ('AB',)

        m = cyc
        # минимальность: проверим, нет ли меньшего делителя-периода
        for d in sorted(_divisors(m)):
            if d < 1:
                continue
            if cap_calls - state["n"] < 1:
                break
            if d < m and call(empty, _R(d)):
                m = d
                break

        confident = True
        if cap_calls - state["n"] >= 1:
            if not call(empty, _R(2 * m)):
                confident = False
        return ('VAL', m, confident)

    except Budget:
        return ('TO',)
